import android.content.BroadcastReceiver;
import android.content.Context;
import android.content.Intent;

import com.github.hfutwifi.database.Profile;
import com.github.hfutwifi.utils.TaskerSettings;
import com.github.hfutwifi.utils.Utils;

import static com.github.hfutwifi.HfutWifiApplication.app;

/**
 * @author CzBiX
 */
public class TaskerReceiver extends BroadcastReceiver {
    @Override
    public void onReceive(Context context, Intent intent) {
        TaskerSettings settings = TaskerSettings.fromIntent(intent);
        Profile profile = app.profileManager.getProfile(settings.profileId);

        if (profile != null) {
             app.switchProfile(settings.profileId);
        }

        if (settings.switchOn) {
            Utils.startSsService(context);
        } else {
            Utils.stopSsService(context);
        }
    }
}
